        function changePage(element) {
            // Remove 'active' class from all links
            document.querySelectorAll('.isi-nav a').forEach(link => link.classList.remove('active'));

            // Add 'active' class to the clicked link
            element.classList.add('active');
        }

        const navElement = document.querySelector('nav');

        navElement.addEventListener('mouseover', function () {
            this.classList.add('scroll-hide-scrollbar');
        });

        navElement.addEventListener('mouseout', function () {
            this.classList.remove('scroll-hide-scrollbar');
        });
